/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  uint8_t i = 0;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  LL_APB2_GRP1_EnableClock(LL_APB2_GRP1_PERIPH_AFIO);
  LL_APB1_GRP1_EnableClock(LL_APB1_GRP1_PERIPH_PWR);

  /* System interrupt init*/
  NVIC_SetPriorityGrouping(NVIC_PRIORITYGROUP_4);

  /* SysTick_IRQn interrupt configuration */
  NVIC_SetPriority(SysTick_IRQn, NVIC_EncodePriority(NVIC_GetPriorityGrouping(),15, 0));

  /** NOJTAG: JTAG-DP Disabled and SW-DP Enabled
  */
  LL_GPIO_AF_Remap_SWJ_NOJTAG();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */
  LL_SYSTICK_EnableIT();

  /* enable PWM outputs */
  LL_TIM_EnableAllOutputs(TIM1);
  LL_TIM_CC_EnableChannel(TIM1, LL_TIM_CHANNEL_CH1 | LL_TIM_CHANNEL_CH2 | LL_TIM_CHANNEL_CH3 | LL_TIM_CHANNEL_CH4);
  LL_TIM_EnableAllOutputs(TIM2);
  LL_TIM_CC_EnableChannel(TIM2, LL_TIM_CHANNEL_CH1 | LL_TIM_CHANNEL_CH2 | LL_TIM_CHANNEL_CH3 | LL_TIM_CHANNEL_CH4);

#ifdef PPM_OVER_UART
  MX_USART1_UART_Init();
#ifdef _DEBUG_
  test_data.checksum = calc_checksum((uint8_t *)(&test_data));
  LL_USART_EnableDMAReq_TX(USART1);
  LL_DMA_EnableChannel(DMA1, LL_DMA_CHANNEL_4);
#endif
#else
  MX_TIM3_Init();
  LL_TIM_CC_EnableChannel(TIM3, LL_TIM_CHANNEL_CH1);
  LL_TIM_EnableCounter(TIM3);
#ifdef _DEBUG_
  MX_TIM4_Init();
  LL_TIM_CC_EnableChannel(TIM4, LL_TIM_CHANNEL_CH3);
  LL_TIM_EnableDMAReq_UPDATE(TIM4);
  LL_DMA_EnableChannel(DMA1, LL_DMA_CHANNEL_7);
  LL_TIM_EnableCounter(TIM4);
#endif
#endif
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1) {
    if (data_ready_flag) {
      data_ready_flag = 0;

#ifdef PPM_OVER_UART
      if (!(pwm_data.sync1 == SYNC_VAL_1 && pwm_data.sync2 == SYNC_VAL_2 && calc_checksum((uint8_t *)(&pwm_data)) == pwm_data.checksum))
        continue;
#endif

      /* set new PWM values */
      LL_TIM_OC_SetCompareCH1(TIM1, normalize_ppm(pwm_data.ch[0]));
      LL_TIM_OC_SetCompareCH2(TIM1, normalize_ppm(pwm_data.ch[1]));
      LL_TIM_OC_SetCompareCH3(TIM1, normalize_ppm(pwm_data.ch[2]));
      LL_TIM_OC_SetCompareCH4(TIM1, normalize_ppm(pwm_data.ch[3]));

      LL_TIM_OC_SetCompareCH1(TIM2, normalize_ppm(pwm_data.ch[4]));
      LL_TIM_OC_SetCompareCH2(TIM2, normalize_ppm(pwm_data.ch[5]));
      LL_TIM_OC_SetCompareCH3(TIM2, normalize_ppm(pwm_data.ch[6]));
      LL_TIM_OC_SetCompareCH4(TIM2, normalize_ppm(pwm_data.ch[7]));

      for (i = 0; i < CH_NUM; i++) {
        if (!h_bridge_ports[i])
          continue;
        /* set H-bridge direction */
        if (pwm_data.ch[i] >= CH_NEUTRAL_VAL)
          LL_GPIO_SetOutputPin(h_bridge_ports[i], h_bridge_pins[i]);
        else
          LL_GPIO_ResetOutputPin(h_bridge_ports[i], h_bridge_pins[i]);
      }

      LL_SYSTICK_DisableIT();  /* critical section */
      pkt_timeout = PACKET_TIMEOUT_MS;  /* reset packet timeout */
      time_elapsed_flag = 0;
      LL_TIM_EnableCounter(TIM1);
      LL_TIM_EnableCounter(TIM2);
      LL_SYSTICK_EnableIT();
    }

    if (time_elapsed_flag) {
      /* stop all PWM outputs in case of packet timeout */
      LL_TIM_DisableCounter(TIM1);
      LL_TIM_DisableCounter(TIM2);
    }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

